package ch02;

public class Student { // 학생 정보(Student) Class 구현
    // 클래스의 속성, 특성을 멤버 변수(Member Variable)라 말한다.
    // 객체를 프로그래밍 하기위해 코드로 정의해 놓은 상태를 클래스(Class)라 말한다.
    int studentNumber; // 학생번호
    String studentName; // 학생이름
    int majorCode; // 전공코드
    String majorName; // 전공이름
    int grade; // 학년
}
